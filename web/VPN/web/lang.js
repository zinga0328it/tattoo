function setLanguage(lang) {
  const t = translations[lang] || translations.en;
  document.querySelector("header h1").textContent = t.title;
  document.querySelector("header p").textContent = t.welcome;
  document.querySelector("main a").textContent = t.download;
  document.querySelector(".intro h1").textContent = t.introTitle;
  const introPs = document.querySelectorAll(".intro p");
  if (introPs[0]) introPs[0].innerHTML = `<strong>${t.introP1}</strong><br>${t.introP2}`;
  if (introPs[1]) introPs[1].innerHTML = t.introP3;
  if (introPs[2]) introPs[2].innerHTML = `<strong>${t.introP4}</strong>`;
  if (introPs[3]) introPs[3].innerHTML = t.introP5;
  const featureList = document.querySelectorAll(".intro ul li");
  t.features.forEach((txt, i) => {
    if (featureList[i]) featureList[i].innerHTML = txt;
  });

  // Sezione: Cosa fa questa VPN
  document.querySelector(".vpn-what-title").textContent = t.vpnWhatTitle;
  const vpnWhatList = document.querySelectorAll(".vpn-what-list li");
  t.vpnWhatList.forEach((txt, i) => { if (vpnWhatList[i]) vpnWhatList[i].innerHTML = txt; });
  document.querySelector(".vpn-what-quote").innerHTML = `<em>${t.vpnWhatQuote}</em>`;

  // Sezione: Romolo RAM Trap
  document.querySelector(".ramtrap-title").textContent = t.ramTrapTitle;
  document.querySelector(".ramtrap-desc").textContent = t.ramTrapDesc;
  const ramTrapFolders = document.querySelectorAll(".ramtrap-folders li");
  t.ramTrapFolders.forEach((txt, i) => { if (ramTrapFolders[i]) ramTrapFolders[i].textContent = txt; });
  document.querySelector(".ramtrap-touch").textContent = t.ramTrapTouch;
  const ramTrapTouchList = document.querySelectorAll(".ramtrap-touch-list li");
  t.ramTrapTouchList.forEach((txt, i) => { if (ramTrapTouchList[i]) ramTrapTouchList[i].textContent = txt; });
  document.querySelector(".ramtrap-noroot").innerHTML = t.ramTrapNoRoot;
  document.querySelector(".ramtrap-quote").textContent = t.ramTrapQuote;

  // Sezione: Gypsy Pack
  document.querySelector(".pack-title").textContent = t.packTitle;
  document.querySelector(".pack-subtitle").textContent = t.packSubtitle;
  const packList = document.querySelectorAll(".pack-list li");
  t.packList.forEach((txt, i) => { if (packList[i]) packList[i].textContent = txt; });
  document.querySelector(".pack-price").textContent = t.packPrice;
  document.querySelector(".pack-button").textContent = t.packButton;
  document.querySelector(".pack-quote").textContent = t.packQuote;

  // Footer
  document.querySelector(".footer").innerHTML = t.footer;
}

