function cambiaLingua(lang) {
  // Rimuovi la classe 'active' da tutte le bandiere
  document.querySelectorAll('.flag').forEach(flag => flag.classList.remove('active'));
  // Aggiungi la classe 'active' alla bandiera selezionata
  document.getElementById('flag-' + lang).classList.add('active');
  // Cambia la lingua
  setStraordinarieLanguage(lang);
}

function setStraordinarieLanguage(lang) {
  console.log('LOG: Inizio setStraordinarieLanguage. Lingua:', lang);
  const t = translations[lang] || translations['it'];
  if (!t) {
    console.error('LOG: Traduzioni non trovate per la lingua:', lang);
    return;
  }
  console.log('LOG: Traduzioni caricate. Chiavi:', Object.keys(t).length);

  try {
    const selectors = {
      'title': { selector: 'title', prop: 'textContent' },
      'h1': { selector: 'h1', prop: 'textContent' },
      'p.subtitle': { selector: 'h1 + p', prop: 'textContent' },
      'h2.cosa-include': { selector: 'h2.title-cosa-include', prop: 'textContent' },
      'h2.prezzo': { selector: 'h2.title-prezzo', prop: 'textContent' },
      'h2.come-funziona': { selector: 'h2.title-come-funziona', prop: 'textContent' },
      'h2.richiesta-rapida': { selector: 'h2.title-richiesta-rapida', prop: 'textContent' },
      'label[for="nome_cliente"]': { selector: 'label[for="nome_cliente"]', prop: 'textContent' },
      'label[for="numero_operatori"]': { selector: 'label[for="numero_operatori"]', prop: 'textContent' },
      'label[for="durata_ore"]': { selector: 'label[for="durata_ore"]', prop: 'textContent' }
    };

    const translationMap = {
      'title': t.title,
      'h1': t.header,
      'p.subtitle': t.subtitle,
      'h2.cosa-include': t.cosa_include,
      'h2.prezzo': t.prezzo,
      'h2.come-funziona': t.come_funziona,
      'h2.richiesta-rapida': t.genera_contratto,
      'label[for="nome_cliente"]': t.nome_cliente,
      'label[for="numero_operatori"]': t.numero_operatori,
      'label[for="durata_ore"]': t.durata_ore
    };

    for (const key in selectors) {
      const el = document.querySelector(selectors[key].selector);
      console.log(`LOG: Cerco selettore '${selectors[key].selector}'... Trovato:`, !!el);
      if (el) {
        const text = translationMap[key];
        if (text !== undefined) {
          console.log(`LOG: Imposto testo per '${key}':`, text);
          el[selectors[key].prop] = text;
        } else {
          console.warn(`LOG: Traduzione non trovata per '${key}'`);
        }
      }
    }

    const liTranslations = [
      t.pulizia_profonda, t.disinfezione, t.trattamento_superfici,
      t.personale_qualificato, t.rapporto_finale, t.pulizia_base, t.pulizia_completa
    ];
    const liList = document.querySelectorAll('li');
    console.log(`LOG: Trovati ${liList.length} elementi <li>`);
    liList.forEach((li, index) => {
      if (liTranslations[index]) {
        console.log(`LOG: Imposto testo per li[${index}]`);
        li.textContent = liTranslations[index];
      }
    });

  } catch (e) {
    console.error('ERRORE CRITICO in setStraordinarieLanguage:', e);
  }
  console.log('LOG: Fine setStraordinarieLanguage.');
}
